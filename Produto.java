package Java;

public class Produto {
    
String nome;
Integer peso;
String preco;


public String getNome() {
    return nome;
}
public void setNome(String nome) {
    this.nome = nome;
}
public Integer getPeso() {
    return peso;
}
public void setPeso(Integer peso) {
    this.peso = peso;
}
public String getPreco() {
    return preco;
}
public void setPreco(String preco) {
    this.preco = preco;
}

}